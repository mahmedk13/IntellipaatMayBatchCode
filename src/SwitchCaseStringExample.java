
public class SwitchCaseStringExample {

	public static void main(String[] args) {
		
		String lang = "Java";
		
		switch(lang) {
		

		
		case "Java":System.out.println("Yes I know Java");
		break;
		case "Javascript":System.out.println("I want to learn it");
		break;
		case "python": System.out.println("I am beginner in python");
		break;
		default: System.out.println("I dont know any prog language");

		
		}

	}

}
