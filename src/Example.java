
public class Example {
	
	
	
	public static void main(String[] args) {
		
		//int i=10;
	//	i++--> post increment
	//  ++i --> pre increment
		
		int i =1;
		int j=1;
		
		System.out.println(i++); //we incremented but value is not updated
		System.out.println(i);
		System.out.println(++j);
		System.out.println(j);
		
	}

}
